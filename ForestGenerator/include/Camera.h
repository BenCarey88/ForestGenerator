#ifndef CAMERA_H_
#define CAMERA_H_

#include <iostream>
#include <vector>
#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @class Camera
/// @brief this class stores the details of the camera for NGLScene
//----------------------------------------------------------------------------------------------------------------------


class Camera
{
public:

  //CONSTRUCTOR
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief default ctor for Camera class
  //--------------------------------------------------------------------------------------------------------------------
  Camera();
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief ctor for camera class that allows us to set different initial from and to vectors
  //--------------------------------------------------------------------------------------------------------------------
  Camera(ngl::Vec3 _initialFrom, ngl::Vec3 _initialTo);

  //MEMBER VARIABLES
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_from;
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector the camera is pointed at
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_to;
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the up direction of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_up;
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the vector for us to rotate around when we move the mouse horizontally
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_axis;
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief initial values of the above (used for camera reset)
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_initialFrom = {0,40,50};
  ngl::Vec3 m_initialTo = {0,10,0};
  ngl::Vec3 m_initialUp = {0,1,0};
  ngl::Vec3 m_initialAxis = {0,1,0};

  //METHODS
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief called to reset the values of the camera
  //--------------------------------------------------------------------------------------------------------------------
  void reset();

};


#endif //CAMERA_H_
