#ifndef CAMERA_H_
#define CAMERA_H_

#include <iostream>
#include <vector>
#include <ngl/Vec3.h>

//----------------------------------------------------------------------------------------------------------------------
/// @class Camera
/// @brief this class stores the details of the camera for NGLScene
//----------------------------------------------------------------------------------------------------------------------


class Camera
{
public:

  //CONSTRUCTOR
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief default ctor for Camera class
  //--------------------------------------------------------------------------------------------------------------------
  Camera() = default;

  //PUBLIC MEMBER VARIABLES
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_from = {0,50,50};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief position vector the camera is pointed at
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_to = {0,10,0};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the nominal up direction of the camera
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_up = {0,1,0};
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief the vector for us to rotate around when we move the mouse horizontally
  //--------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_axis = {0,1,0};

  //PUBLIC METHODS
  //--------------------------------------------------------------------------------------------------------------------
  /// @brief called to reset the values of the camera
  //--------------------------------------------------------------------------------------------------------------------
  void reset();

};


#endif //CAMERA_H_
